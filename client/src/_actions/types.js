export const LOGIN_USER = "login_user";
export const REGISTER_USER = "register_user";
export const AUTH_USER = "auth_user";
export const LOGOUT_USER = "logout_user";
export const ADD_TO_CART = "add_to_cart";
export const GET_CART_ITEMS = "get_cart_items";
export const REMOVE_CART_ITEM = "remove_cart_item";
export const ON_SUCCESS_BUY = "on_success_buy";

// 위 코드는 Redux 애플리케이션에서 사용되는 액션 타입 상수를 정의한 것입니다.
// LOGIN_USER: 사용자 로그인 액션을 나타냅니다.
// REGISTER_USER: 사용자 가입 액션을 나타냅니다.
// AUTH_USER: 사용자 인증 액션을 나타냅니다.
// LOGOUT_USER: 사용자 로그아웃 액션을 나타냅니다.
// ADD_TO_CART: 장바구니에 상품 추가 액션을 나타냅니다.
// GET_CART_ITEMS: 장바구니에 있는 상품 목록 가져오기 액션을 나타냅니다.
// REMOVE_CART_ITEM: 장바구니에서 상품 제거 액션을 나타냅니다.
// ON_SUCCESS_BUY: 결제가 완료되었을 때 발생하는 액션을 나타냅니다.
// Redux에서는 상태(state)를 변경하는 액션(action)을 정의할 때 이러한 액션 타입 상수를 사용합니다.
